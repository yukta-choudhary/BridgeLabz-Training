// This program calculates age of Harry when current year is 2024.
public class CalculateAge{
    public static void main(String[] args){

        // Create a variable ageHarry, birthYearHarry and currentYear=2024
        int birthYearHarry = 2000, currentYear = 2024, ageHarry;

        // Calculate age of Harry
        ageHarry = currentYear - birthYearHarry;

        // Print the age of Harry
        System.out.println("Age of Harry is " + ageHarry);
    }
}